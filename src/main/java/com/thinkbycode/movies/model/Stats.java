package com.thinkbycode.movies.model;

public class Stats {

	private int movies;
	private int documentaries;
	private int series;
	
	public int getMovies() {
		return movies;
	}
	public void setMovies(int movies) {
		this.movies = movies;
	}
	public int getDocumentaries() {
		return documentaries;
	}
	public void setDocumentaries(int documentaries) {
		this.documentaries = documentaries;
	}
	public int getSeries() {
		return series;
	}
	public void setSeries(int series) {
		this.series = series;
	}
	
	
}
